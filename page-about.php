        <h3>A Brief History: </h3>
        <p>The running club, which was originally called Memphis Striders, was informally organized in 1975 by Bud Joyner, who was proprietor of the first running store in Memphis. Bud's wife thought that Memphis Runner's Track Club sounded better than Memphis Striders, so that became the name. Judge John Colton was the first member, and the late Dr. Jack Rockett was the second.</p>
 
        <p>The club went along on this informal basis for three years until 1979 when it was officially incorporated.  Jack Rockett, Mike Cody and Bill Butler met at the Healthy Trading Food Store and flipped a coin to see who had to be the first President. Jack lost the toss and thus became the first president of the Memphis Runner's Track Club.  Mike Cody was tasked with being the first Vice President.  Bill Butler was the club's first editor; and the first newsletter was sent to press in August,1979.</p>

